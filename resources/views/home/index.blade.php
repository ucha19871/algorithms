    <p>
        In mathematics and computer science, <br>
        an algorithm is a self-contained step-by-step set of operations to be performed. <br>
        Algorithms exist that perform calculation, data processing, and automated reasoning. <br>
        An algorithm is an effective method that can be expressed within a finite amount of space and time <br> 
        and in a well-defined formal language for calculating a function. 
        Starting from an initial state and initial input,<br> 
        the instructions describe a computation that, when executed, <br>
        proceeds through a finite number of well-defined successive states, <br>
        eventually producing "output" and terminating at a final ending state. <br>
        The transition from one state to the next is not necessarily deterministic<br> 
        some algorithms, known as randomized algorithms, incorporate random input.<br><br>
        
        The concept of algorithm has existed for centuries, however a partial formalization of what would become the modern algorithm <br>
        began with attempts to solve the Entscheidungsproblem (the "decision problem") posed by David Hilbert in 1928. <br>
        Subsequent formalizations were framed as attempts to define "effective calculability"<br> 
        or "effective method". those formalizations included the Gödel–Herbrand–Kleene recursive functions of 1930, 1934 and 1935, <br>
        Alonzo Church's lambda calculus of 1936, Emil Post's "Formulation 1" of 1936, <br>
        and Alan Turing's Turing machines of 1936–7 and 1939. Giving a formal definition of algorithms, <br>
        corresponding to the intuitive notion, remains a challenging problem.<br>
     
